class Calc:
    def getSumSum(self,
                  a: int,
                  b: int,
                  c: int) -> int:
        return a + b + c

    def get_sum(self, a, b):
        return a + b

    def getDivide(self, a, b):
        if b != 0:
            return a / b
        return 0
      
    def getGop(self, a, b):
        return a * b

